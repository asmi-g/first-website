const home_hover = document.getElementById('home_hover');

home_hover.addEventListener('mouseover', function handleMouseOver()
{
    home_hover.style.color = '#6247aa';
    home_hover.style.paddingBlock = "5px";
    home_hover.style.paddingLeft = "5px";
    home_hover.style.paddingRight = "5px";
    home_hover.style.backgroundColor =  '#E2CFEA';
}
);

home_hover.addEventListener('mouseout', function handleMouseOut()
{
    home_hover.style.color = 'white';
    home_hover.style.paddingBlock = "0px";
    home_hover.style.paddingLeft = "0px";
    home_hover.style.paddingRight = "0px";
    home_hover.style.backgroundColor =  'transparent';
}
);


